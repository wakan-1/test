function showPost(postId) {
    selectedPost = posts.find(p => p.id === postId);
    if (!selectedPost) return;

    const postDetail = document.getElementById('post-detail');
    postDetail.innerHTML = `
        <span class="post-category">${selectedPost.category}</span>
        <h1 class="post-title">${selectedPost.title}</h1>
        <div class="post-meta">
            <span class="post-date">๐ ${formatDate(selectedPost.date)}</span>
        </div>
        <div class="post-body">
            ${selectedPost.body}
        </div>
    `;

    showPage('post');
}

function formatDate(dateString) {
    const date = new Date(dateString);
    return date.toLocaleDateString('ar-SA', {
        year: 'numeric',
        month: 'long',
        day: 'numeric'
    });
}

function showPage(pageName) {
    document.querySelectorAll('.page').forEach(page => {
        page.classList.remove('active');
    });

    document.querySelectorAll('.nav-link').forEach(link => {
        link.classList.remove('active');
    });

    if (pageName === 'post') {
        document.getElementById('post-page').classList.add('active');
    } else {
        document.getElementById(`${pageName}-page`).classList.add('active');
        const activeLink = document.querySelector(`[data-page="${pageName}"]`);
        if (activeLink) {
            activeLink.classList.add('active');
        }
    }

    currentPage = pageName;
}

document.querySelectorAll('.nav-link').forEach(link => {
    link.addEventListener('click', (e) => {
        e.preventDefault();
        const page = link.getAttribute('data-page');
        showPage(page);
    });
});

document.getElementById('back-btn').addEventListener('click', () => {
    showPage('home');
});

renderPosts();
